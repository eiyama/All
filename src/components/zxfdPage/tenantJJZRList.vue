        <p class="t52">第二十五条  被保险人向保险人请求赔偿的诉讼时效期间为二年，自其知道或者应当知道保险事故发生之日起计算。</p>

        <h3>争议处理</h3>
        <p class="t52">第二十六条  因履行本保险合同发生争议的，由当事人协商解决。协商不成的，提交保险单载明的仲裁机构仲裁。保险单未载明仲裁机构且发生争议后未达成仲裁协议的，可向中华人民共和国人民法院起诉。</p>
        <p class="t52">第二十七条  本保险合同的争议处理适用中华人民共和国法律（不包括港澳台地区法律）。</p>

        <h3>其他事项</h3>
        <p class="t52">第二十八条	保险责任开始前，投保人要求解除保险合同的，应当向保险人支付相当于保险费5%的退保手续费，保险人应当退还剩余部分保险费；保险人要求解除保险合同的，不得向投保人收取手续费并应退还已收取的保险费。
        保险责任开始后，投保人要求解除保险合同的，自通知保险人之日起，保险合同解除，保险人按短期费率计收保险责任开始之日起至合同解除之日止期间的保险费，并退还剩余部分保险费；保险人也可提前十五日向投保人发出解约通知书解除本保险合同，保险人按照保险责任开始之日起至合同解除之日止期间与保险期间的日比例计收保险费，并退还剩余部分保险费。</p>
