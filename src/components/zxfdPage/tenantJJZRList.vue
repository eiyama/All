        <h3 style="margin-top: 30px;">居家责任保险条款</h3>
        <h3>总则</h3>
        <p class="t52">第一条 本保险合同由保险条款、投保单、保险单以及批单组成。凡涉及本保险合同的约定，均应采用书面形式。</p>
        <h3>保险责任</h3>
        <p
          class="t52"
        >第二条 在保险期间内，被保险人或其家庭成员在本保险单载明的被保险人住址内，因过失造成第三者的人身伤亡或财产损失，依法应由被保险人承担的赔偿责任，保险人按照保险合同约定在本保险单载明的责任限额内负责赔偿。</p>
        <p
          class="t52"
        >第三条 被保险人因第二条所列原因给第三者造成损害而被提起仲裁或者诉讼的，对应由被保险人支付的仲裁或者诉讼费用以及经保险人事先书面同意的律师费用，保险人也负责赔偿；赔偿的数额在保险单载明的每次事故责任限额以外另行计算，最高不超过每次事故责任限额的30%。</p>
        <h3>责任免除</h3>
        <p class="t52">第四条 下列原因造成的损失、费用和责任，保险人不负责赔偿：</p>
        <p class="t52">（一）被保险人或其家庭成员的故意行为；</p>
        <p class="t52">（二）战争、敌对行为、军事行为、武装冲突、恐怖活动、罢工、骚乱、暴动；</p>
        <p class="t52">（三）核反应、核子辐射和放射性污染。</p>
        <p class="t52">第五条 对于下列各项损失、费用和责任，保险人不负责赔偿：</p>
        <p class="t52">（一）被保险人或其家庭成员从事职业、职务行为，或为他人提供商业性质的服务时造成第三者人身伤亡和财产损失；</p>
        <p class="t52">（二）被保险人或其家庭成员在精神错乱、智障状态下所造成的第三者人身伤亡或财产损失；</p>
        <p class="t52">（三）被保险人或其家庭成员、家政服务人员的人身伤亡和财产损失；</p>
        <p class="t52">（四）因被保险人所雇佣的家政服务人员引起的任何赔偿责任；</p>
        <p class="t52">（五） 被保险人或其家庭成员所拥有、照管的动物造成的人身伤亡或财产损失；</p>
        <p class="t52">（六）被保险人与第三方订立的合同项下应承担的合同责任，但即使没有这种合同，被保险人仍应承担的责任不在此限；</p>
        <p class="t52">（七）被保险人或其家庭成员因拥有或使用各种机动车、船及飞行器导致的损失和责任；</p>
        <p class="t52">（八）任何类型的传染病导致的损失和责任；</p>
        <p class="t52">（九）精神损害赔偿责任；</p>
        <p class="t52">（十）保险事故发生后引起的各种间接损失或责任；</p>
