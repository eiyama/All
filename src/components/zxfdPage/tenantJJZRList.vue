        <h3>责任限额</h3>
        <p class="t52">第七条 每次事故责任限额和累计事故责任限额由投保人自行确定。</p>
        <h3>保险期间</h3>
        <p class="t52">第八条 除合同另有约定外，保险期间为一年，以保险单载明的起迄时间为准。</p>
        <h3>保险人义务</h3>
        <p class="t52">第九条 本保险合同成立后，保险人应当及时向投保人签发保险单或其他保险凭证。</p>
        <p class="t52">
          第十条 保险人依本保险条款第十四条取得的合同解除权，自保险人知道有解除事由之日起，超过三十日不行使而消灭。
          保险人在保险合同订立时已经知道投保人未如实告知的情况的，保险人不得解除合同；发生保险事故的，保险人应当承担赔偿责任。
