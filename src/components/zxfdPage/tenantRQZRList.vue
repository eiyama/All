          class="t52"
        >第二十三条 被保险人被提起或获悉可能发生诉讼、仲裁时，应立即以书面形式通知保险人；接到法院传票或其他法律文书后，应将其副本及时送交保险人。保险人有权以被保险人的名义处理有关诉讼或仲裁事宜，被保险人应提供有关文件，并给予必要的协助。</p>
        <p class="t52">对因未及时提供上述通知或必要协助引起或扩大的损失，保险人不承担赔偿责任。</p>
        <p class="t52">第二十四条 被保险人向保险人请求赔偿时，应提交以下材料：</p>
        <p class="t52">（一）保险单或保险凭证；</p>
        <p class="t52">（二）索赔申请书；</p>
        <p class="t52">（三）消防部门事故证明；</p>
        <p class="t52">（四）医疗鉴定部门伤残程度鉴定证明材料；</p>
        <p class="t52">（五）二级及以上医院出具的病历、检查检验报告、诊断记录等医疗证明、住院医疗费用原始单据、发票；身故的，应提供公安机关出具的死亡证明；</p>
        <p class="t52">（六）经裁判或仲裁的，应提交裁判或仲裁文书；</p>
        <p class="t52">（七）投保人、被保险人所能提供的其他与确认保险事故的性质、原因、损失程度等有关的证明和资料。</p>
