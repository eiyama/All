        <p class="t52">第二十五条 发生保险责任范围内的损失，应由有关责任方负责赔偿的，被保险人应行使或保留向该责任方请求赔偿的权利。</p>
        <p class="t52">保险事故发生后，保险人未履行赔偿义务之前，被保险人放弃对有关责任方请求赔偿的权利的，保险人不承担赔偿责任。</p>
        <p class="t52">保险人向被保险人赔偿保险金后，被保险人未经保险人同意放弃对有关责任方请求赔偿的权利的，该行为无效。</p>
        <p class="t52">在保险人向有关责任方行使代位请求赔偿权利时，被保险人应当向保险人提供必要的文件和其所知道的有关情况。</p>
        <p class="t52">由于被保险人的故意或者重大过失致使保险人不能行使代位请求赔偿的权利的，保险人可以扣减或者要求返还相应的赔偿金额。</p>

        <h3>赔偿处理</h3>
        <p class="t52">第二十六条 保险人的赔偿以下列方式之一确定的被保险人的赔偿责任为基础：</p>
        <p class="t52">（一）被保险人和向其提出损害赔偿请求的有关人员协商并经保险人确认；</p>
        <p class="t52">（二）仲裁机构裁决；</p>
        <p class="t52">（三）人民法院判决；</p>
        <p class="t52">（四）保险人认可的其他方式。</p>
        <p class="t52">第二十七条 发生保险责任范围内的损失，被保险人未向第三者赔偿的，保险人不负责向被保险人赔偿保险金。</p>
        <p class="t52">第二十八条 在本保险期间内，发生保险责任范围内的损失，保险人按下列方式计算赔偿：</p>
        <p class="t52">（一）对于每次事故造成的损失，保险人在本保险单约定的每次事故责任限额内计算赔偿，其中对每人人身伤亡的赔偿金额不得超过每人人身伤亡责任限额。</p>
        <p class="t52">1、死亡：在保险单约定的每人人身伤亡责任限额内据实赔偿；</p>
        <p class="t52">2、伤残（含烧伤）：依保险人认可的医疗机构出具的伤残程度证明，在保险合同所附《人身伤残程度与赔偿比例表》规定的百分比乘以每人人身伤亡责任限额的数额内赔偿;</p>
        <p class="t52">3、医疗费：按当地社会保险主管部门规定可报销的医疗费用（包括药品目录和医疗设备目录），在每人医疗费用责任限额内计算赔偿。</p>
        <p class="t52">（二）被保险人不得就其单个伤者因同一保险事故同时申请伤残赔偿金和死亡赔偿金。</p>
        <p class="t52">（三）保险人对每次事故的人身伤亡、医疗费用支出及财产损失的赔偿金额之和不超过本保险单约定的每次事故责任限额。</p>
        <p class="t52">（四）在保险期间内，保险人对多次事故损失的累计赔偿金额不超过累计责任限额。</p>
        <p
          class="t52"
        >第二十九条 发生保险事故时，如果被保险人的损失能够从其他相同保障的保险项下也获得赔偿，则本保险人按照本保险合同的累计责任限额与所有有关保险合同的累计责任限额总和的比例承担赔偿责任。其他保险人应承担的赔偿金额，本保险人不负责垫付。</p>
        <p
          class="t52"
        >被保险人在请求赔偿时应当如实向保险人说明与本保险合同保险责任有关的其他保险合同的情况。对未如实说明导致保险人多支付保险金的，保险人有权向被保险人追回多支付的部分。</p>
        <p
          class="t52"
        >第三十条 被保险人及其代表在燃气事故发生后不履行救护职责或逃逸，以及无力支付伤者医疗抢救费用等特殊情况下，经事故调查属于保险责任的，对符合当地社会保险医疗保险主管部门规定的医疗抢救费用，由被保险人提出书面申请，或经县级以上政府燃气安全管理部门要求后由受害人或其家属提出书面申请，保险人可在保险单约定的每人医疗费用责任限额内，按照每人医疗费用责任限额的30%预付。</p>
        <p class="t52">被保险人或受害人申请预付赔款，需提交的材料包括：预付赔款申请，县级以上政府燃气安全管理部门证明等。</p>
        <p class="t52">第三十一条 保险人根据不同情况，按照以下两种方式支付赔款：</p>
        <p class="t52">（一）被保险人已经支付赔款给事故受害人的，保险人对依法应由被保险人承担的损失按照本保险合同的约定进行赔偿；</p>
        <p
          class="t52"
        >（二）被保险人及其代表在事故发生后死亡、失去行为能力或逃逸的，经保险人调查核实，并由县级以上政府燃气安全管理部门及公安部门认可证明，事故受害人或其法定继承人可以直接向保险人提出索赔，保险人按本保险合同的约定支付赔款。</p>
        <p class="t52">第三十二条 被保险人向保险人请求赔偿的诉讼时效期间为二年，自其知道或者应当知道保险事故发生之日起计算。</p>
        <h3>争议处理</h3>
