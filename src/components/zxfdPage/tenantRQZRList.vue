        <p class="t52">（四）大气污染、土地污染、水污染及其他各种污染；</p>
        <p class="t52">（五）行政行为或司法行为；</p>
        <p class="t52">（六）燃气用户违反有关法规及燃气安全使用规定，擅自拆卸管道、表阀、灶具；</p>
