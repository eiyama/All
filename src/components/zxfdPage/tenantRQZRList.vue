        <p class="t52">第二十条 在保险期间内，保险标的危险程度显著增加的，被保险人应及时书面通知保险人，保险人有权要求增加保险费或者解除合同。</p>
        <p class="t52">被保险人未履行通知义务，因上述保险标的危险程度显著增加而导致保险事故发生的，保险人不承担赔偿责任。</p>
        <p class="t52">第二十一条 发生保险责任范围内的事故，被保险人应该：</p>
        <p class="t52">（一）尽力采取必要、合理的措施，防止或减少损失，否则，对因此扩大的损失，保险人不承担赔偿责任；</p>
