        <p
          class="t52"
        >第二十二条 被保险人收到第三者的损害赔偿请求时，应立即通知保险人。未经保险人书面同意，被保险人对受害人及其代理人作出的任何承诺、拒绝、出价、约定、付款或赔偿，保险人不受其约束。对于被保险人自行承诺或支付的赔偿金额，保险人有权重新核定，不属于本保险责任范围或超出应赔偿限额的，保险人不承担赔偿责任。在处理索赔过程中，保险人有权自行处理由其承担最终赔偿责任的任何索赔案件，被保险人有义务向保险人提供其所能提供的资料和协助。</p>
