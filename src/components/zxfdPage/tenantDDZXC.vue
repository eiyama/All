        <p class="t52">投保人因重大过失未履行如实告知义务，对保险事故的发生有严重影响的，保险人对于合同解除前发生的保险事故，不承担赔偿责任，但应当退还保险费。 </p>
        <p class="t52">第十八条  投保人应在签订保险合同时一次交清保险费。保险费交付前发生的保险事故，保险人不承担赔偿责任。 </p>
