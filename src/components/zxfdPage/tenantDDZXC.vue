        <p class="t52">第三条	本保险合同中的第三者是指除投保人、被保险人、保险人以外的，因保险电动自行车发生意外事故遭受人身伤亡或财产损失的受害者。 </p>
        <h3>保险责任</h3>
        <p class="t52">第四条	在保险期间内，被保险人或其年满16周岁的家庭成员在使用保险电动自行车过程中发生意外事故，致使第三者遭受人身伤亡或财产直接损毁，依法应当由被保险人承担的经济赔偿责任，保险人按照本保险合同的约定负责赔偿。</p>
        <p class="t52">第五条	保险事故发生后，被保险人因保险事故而被提起仲裁或者诉讼的，对应由被保险人支付的仲裁或诉讼费用以及其它必要的、合理的费用，经保险人事先书面同意，保险人按本保险合同的约定也负责赔偿。
        </p>
        <h3>责任免除</h3>
        <p class="t52"> 第六条	下列情况下，不论任何原因造成的对第三者的经济赔偿责任，保险人均不负责赔偿：</p>
        <p class="t52">（一）利用保险电动自行车从事违法活动；</p>
        <p class="t52">（二）被保险人或其家庭成员饮酒、吸食或注射毒品、被药物麻醉后使用保险电动自行车；</p>
        <p class="t52">（三）保险电动自行车在竞赛或在营业性维修场所修理期间；</p>
        <p class="t52">（四）保险电动自行车因私自改装、加装致使整车性能不符合国家规定的标准。</p>
        <p class="t52">第七条 对于下列各项损失、费用和责任，保险人不负责赔偿：</p>
        <p class="t52">（一）被保险人及其家庭成员的人身伤亡、所有或代管的财产损失；</p>
        <p class="t52">（二）保险电动自行车上其他人员的人身伤亡或财产损失；</p>
        <p class="t52">（三）被保险人或其家庭人员的故意行为造成的损失；</p>
        <p class="t52">（四）因污染（含放射性污染）造成的损失；</p>
        <p class="t52">（五）精神损害赔偿；</p>
        <p class="t52">（六）任何间接损失；</p>
        <p class="t52">（七）罚款、罚息及惩罚性赔偿。</p>
        <p class="t52">第八条	其它不属于保险责任范围内的损失、费用和责任，保险人不负责赔偿。责任限额与免赔额</p>
        <p class="t52">第九条	责任限额包括每次事故责任限额和累计责任限额，由投保人自行确定，并在保险合同中载明。</p>
