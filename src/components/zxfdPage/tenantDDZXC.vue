        <p class="t52">（二）在保险期间内，保险人对多次事故损失的累计赔偿金额最高不超过累计责任限额。</p>
        <p class="t52">第二十八条 对每次事故法律费用的赔偿金额，保险人在第二十七条计算的赔偿金额以外按应由被保险人支付的数额另行计算。</p>
        <p class="t52">第二十九条  发生保险事故时，如果被保险人的损失能够从其他相同保障的保险项下也获得赔偿，则本保险人按照本保险合同的责任限额与所有有关保险合同的责任限额总和的比例承担赔偿责任。其他保险人应承担的赔偿金额，本保险人不负责垫付。
        被保险人在请求赔偿时应当如实向保险人说明与本保险合同保险责任有关的其他保险合同的情况。对未如实说明导致保险人多支付保险金的，保险人有权向被保险人追回多支付的部分。</p>
        <p class="t52">第三十条  被保险人向保险人请求赔偿的诉讼时效期间为二年，自其知道或者应当知道保险事故发生之日起计算。</p>

        <h3>争议处理</h3>
        <p class="t52"> 第三十一条  因履行本保险合同发生争议的，由当事人协商解决。协商不成的，提交保险单载明的仲裁机构仲裁。保险单未载明仲裁机构且发生争议后未达成仲裁协议的，可向中华人民共和国人民法院起诉。</p>
        <p class="t52">第三十二条	本保险合同的争议处理适用中华人民共和国法律（不包括港澳台地区法律）。</p>

